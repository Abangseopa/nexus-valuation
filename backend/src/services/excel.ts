function addLBOModelSheet(
  wb: ExcelJS.Workbook,
  fd: FinancialData,
  result: LBOResult
): void {
  const ws = wb.addWorksheet('LBO Model');
  ws.views = [{ state: 'frozen', xSplit: 1, ySplit: 2 }];

  const yearLabels = result.years.map(y => `Year ${y.year}`);
  setColumnWidths(ws, [32, ...yearLabels.map(() => 16)]);

  const titleRow = ws.addRow(['LBO MODEL', ...yearLabels]);
  styleRow(titleRow, C.headerBg, C.headerFg, true);
  ws.addRow([]);

  modelSectionHeader(ws, 'INCOME STATEMENT', yearLabels.length);
  modelRow(ws, 'Revenue',           result.years.map(y =>  y.revenue           / 1e9), FMT.billions, false);
  modelRow(ws, 'EBITDA',            result.years.map(y =>  y.ebitda            / 1e9), FMT.billions, false);
  modelRow(ws, 'EBIT',              result.years.map(y =>  y.ebit              / 1e9), FMT.billions, false);
  modelRow(ws, '  Interest Expense',result.years.map(y => -y.interestExpense   / 1e9), FMT.billions, false);
  modelRow(ws, 'EBT',               result.years.map(y =>  y.ebt              / 1e9), FMT.billions, false);
  modelRow(ws, '  Taxes',           result.years.map(y => -y.taxes             / 1e9), FMT.billions, false);
  modelRow(ws, 'Net Income',        result.years.map(y =>  y.netIncome         / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'DEBT SCHEDULE', yearLabels.length);
  modelRow(ws, 'Free Cash Flow',    result.years.map(y =>  y.freeCashFlow      / 1e9), FMT.billions, false);
  modelRow(ws, 'Debt Repaid',       result.years.map(y =>  y.debtRepaid        / 1e9), FMT.billions, false);
  modelRow(ws, 'Ending Debt',       result.years.map(y =>  y.endingDebt        / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'RETURNS', yearLabels.length);

  const returnsData: [string, number][] = [
    ['Entry Equity',           result.entryEquity          / 1e9],
    ['Exit EBITDA',            result.exitEBITDA            / 1e9],
    ['Exit Enterprise Value',  result.exitEnterpriseValue  / 1e9],
    ['Exit Debt',              result.exitDebt              / 1e9],
    ['Exit Equity',            result.exitEquity            / 1e9],
    ['MOIC',                   result.moic],
    ['IRR',                    result.irr],
  ];

  for (const [label, val] of returnsData) {
    const fmt = label === 'MOIC' ? FMT.mult : label === 'IRR' ? FMT.pct1 : FMT.billions;
    const r = ws.addRow([label, val]);
    r.getCell(1).font = { bold: label === 'MOIC' || label === 'IRR' };
    r.getCell(2).numFmt = fmt;
    r.getCell(2).font  = { bold: label === 'MOIC' || label === 'IRR' };
    if (label === 'MOIC' || label === 'IRR') fillRow(r, C.totalBg, 2);
    applyBorders(r, 2);
  }
}


