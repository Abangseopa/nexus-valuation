/** Build a DCF workbook and return it as a Buffer for upload. */
export async function buildDCFExcel(
  financialData: FinancialData,
  result: DCFResult
): Promise<Buffer> {
  const wb = new ExcelJS.Workbook();
  wb.creator  = 'Nexus Valuation';
  wb.created  = new Date();

  addDCFSummarySheet(wb, financialData, result);
  addDCFAssumptionsSheet(wb, result);
  addDCFModelSheet(wb, financialData, result);
  addHistoricalSheet(wb, financialData);

  return Buffer.from(await wb.xlsx.writeBuffer());
}

/** Build an LBO workbook and return it as a Buffer for upload. */
export async function buildLBOExcel(
  financialData: FinancialData,
  result: LBOResult
): Promise<Buffer> {
  const wb = new ExcelJS.Workbook();
  wb.creator  = 'Nexus Valuation';
  wb.created  = new Date();

  addLBOSummarySheet(wb, financialData, result);
  addLBOAssumptionsSheet(wb, result);
  addLBOModelSheet(wb, financialData, result);
  addHistoricalSheet(wb, financialData);

  return Buffer.from(await wb.xlsx.writeBuffer());
}


