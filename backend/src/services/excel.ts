function addDCFSummarySheet(
  wb: ExcelJS.Workbook,
  fd: FinancialData,
  result: DCFResult
): void {
  const ws = wb.addWorksheet('Summary');
  ws.views = [{ state: 'frozen', ySplit: 1 }];
  setColumnWidths(ws, [30, 20, 20]);

  // Title
  mergeHeader(ws, `${fd.company.name} (${fd.company.ticker}) — DCF Valuation`, 1, 3, 1);
  mergeHeader(ws, `Generated by Nexus Valuation | ${new Date().toLocaleDateString()}`, 2, 3, 2);

  ws.addRow([]);

  // Key outputs table
  sectionHeader(ws, 'KEY OUTPUTS', 3);
  const rows: [string, number, string][] = [
    ['Enterprise Value',     result.enterpriseValue, FMT.billions],
    ['Net Debt',             result.netDebt,          FMT.billions],
    ['Equity Value',         result.equityValue,      FMT.billions],
    ['PV of Forecast FCFs',  result.years.reduce((s, y) => s + y.pvUFCF, 0), FMT.billions],
    ['PV of Terminal Value', result.pvTerminalValue,  FMT.billions],
    ['Terminal Value',       result.terminalValue,    FMT.billions],
  ];
  for (const [label, val, fmt] of rows) {
    outputRow(ws, label, val / 1e9, fmt);
  }

  ws.addRow([]);
  sectionHeader(ws, 'VALUATION BRIDGE', 3);
  outputRow(ws, 'WACC',                result.assumptions.wacc,               FMT.pct1);
  outputRow(ws, 'Terminal Growth Rate', result.assumptions.terminalGrowthRate, FMT.pct1);
  outputRow(ws, 'Forecast Years',       result.assumptions.forecastYears,      FMT.integer);

  ws.addRow([]);
  sectionHeader(ws, 'TV AS % OF TOTAL EV', 3);
  const tvPct = result.pvTerminalValue / result.enterpriseValue;
  outputRow(ws, 'Terminal Value %', tvPct, FMT.pct1);
}


function addDCFAssumptionsSheet(wb: ExcelJS.Workbook, result: DCFResult): void {
  const ws = wb.addWorksheet('Assumptions');
  setColumnWidths(ws, [35, 20]);

  mergeHeader(ws, 'DCF ASSUMPTIONS', 1, 2, 1);
  ws.addRow([]);

  sectionHeader(ws, 'DISCOUNT RATE', 2);
  inputRow(ws, 'WACC',                    result.assumptions.wacc,               FMT.pct2);
  inputRow(ws, 'Terminal Growth Rate',     result.assumptions.terminalGrowthRate, FMT.pct2);

  ws.addRow([]);
  sectionHeader(ws, 'INCOME STATEMENT', 2);
  inputRow(ws, 'EBITDA Margin',           result.assumptions.ebitdaMargin,            FMT.pct1);
  inputRow(ws, 'Tax Rate',                result.assumptions.taxRate,                 FMT.pct1);

  ws.addRow([]);
  sectionHeader(ws, 'CAPITAL STRUCTURE', 2);
  inputRow(ws, 'CapEx as % of Revenue',   result.assumptions.capexAsPercentOfRevenue, FMT.pct1);
  inputRow(ws, 'NWC as % of Revenue',     result.assumptions.nwcAsPercentOfRevenue,   FMT.pct1);

  ws.addRow([]);
  sectionHeader(ws, 'REVENUE GROWTH', 2);
  result.assumptions.revenueGrowthRates.forEach((r, i) => {
    inputRow(ws, `Year ${i + 1} Growth`, r, FMT.pct1);
  });
}


function addDCFModelSheet(
  wb: ExcelJS.Workbook,
  fd: FinancialData,
  result: DCFResult
): void {
  const ws = wb.addWorksheet('DCF Model');
  ws.views = [{ state: 'frozen', xSplit: 1, ySplit: 3 }];

  const yearLabels = result.years.map(y => `Year ${y.year}`);
  setColumnWidths(ws, [32, ...yearLabels.map(() => 16)]);

  // ── Headers ──────────────────────────────────────────────
  const titleRow = ws.addRow(['DCF MODEL — UNLEVERED FREE CASH FLOW', ...yearLabels]);
  styleRow(titleRow, C.headerBg, C.headerFg, true);

  const baseRow = ws.addRow(['Base Revenue', result.baseRevenue / 1e9]);
  styleRow(baseRow, C.subheaderBg, C.subheaderFg, false);
  baseRow.getCell(2).numFmt = FMT.billions;
  ws.addRow([]);

  // ── Income section ────────────────────────────────────────
  modelSectionHeader(ws, 'INCOME STATEMENT', yearLabels.length);

  modelRow(ws, 'Revenue',               result.years.map(y => y.revenue   / 1e9), FMT.billions, false);
  modelRow(ws, 'EBITDA',                result.years.map(y => y.ebitda    / 1e9), FMT.billions, false);
  modelRow(ws, '  D&A',                 result.years.map(y => -y.da       / 1e9), FMT.billions, false);
  modelRow(ws, 'EBIT',                  result.years.map(y => y.ebit      / 1e9), FMT.billions, true);
  modelRow(ws, 'NOPAT (EBIT × (1-t))', result.years.map(y => y.nopat     / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'FREE CASH FLOW BRIDGE', yearLabels.length);
  modelRow(ws, 'NOPAT',              result.years.map(y =>  y.nopat     / 1e9), FMT.billions, false);
  modelRow(ws, '  + D&A',           result.years.map(y =>  y.da        / 1e9), FMT.billions, false);
  modelRow(ws, '  - CapEx',         result.years.map(y => -y.capex     / 1e9), FMT.billions, false);
  modelRow(ws, '  - ΔNWC',          result.years.map(y => -y.changeInNWC / 1e9), FMT.billions, false);
  modelRow(ws, 'Unlevered FCF',     result.years.map(y =>  y.ufcf      / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'DISCOUNTING', yearLabels.length);
  modelRow(ws, 'Discount Factor',   result.years.map(y => y.discountFactor), FMT.pct2, false);
  modelRow(ws, 'PV of UFCF',       result.years.map(y => y.pvUFCF    / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'VALUATION SUMMARY', yearLabels.length);

  const summaryItems: [string, number][] = [
    ['Sum of PV(FCFs)',     result.years.reduce((s, y) => s + y.pvUFCF, 0) / 1e9],
    ['PV of Terminal Value', result.pvTerminalValue / 1e9],
    ['Enterprise Value',    result.enterpriseValue  / 1e9],
    ['Less: Net Debt',     -result.netDebt          / 1e9],
    ['Equity Value',        result.equityValue      / 1e9],
  ];
  for (const [label, val] of summaryItems) {
    const r = ws.addRow([label, val]);
    r.getCell(1).font = { bold: label === 'Equity Value' || label === 'Enterprise Value' };
    r.getCell(2).numFmt = FMT.billions;
    r.getCell(2).font = { bold: label === 'Equity Value' || label === 'Enterprise Value' };
    if (label === 'Equity Value') fillRow(r, C.totalBg, 2);
    applyBorders(r, 2);
  }
}


