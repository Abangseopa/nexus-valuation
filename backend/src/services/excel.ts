import ExcelJS from 'exceljs';
import type { DCFResult, LBOResult } from './valuation';
import type { FinancialData } from '../types';

// ─── Colour palette ───────────────────────────────────────────────────────────
const C = {
  headerBg:    '1F3864',   // dark navy  — section headers
  headerFg:    'FFFFFF',   // white text
  subheaderBg: '2E75B6',   // mid blue   — sub-headers
  subheaderFg: 'FFFFFF',
  inputBg:     'FFF2CC',   // amber      — assumption / input cells
  inputFg:     '000000',
  calcBg:      'FFFFFF',   // white      — calculated outputs
  totalBg:     'D6E4F0',   // light blue — subtotal / total rows
  negativeFg:  'C00000',   // red        — negative numbers
  borderColor: 'BFBFBF',
} as const;

// ─── Number formats ───────────────────────────────────────────────────────────
const FMT = {
  billions: '#,##0.0"B"',
  millions: '#,##0.0"M"',
  pct1:     '0.0%',
  pct2:     '0.00%',
  mult:     '0.0"x"',
  integer:  '#,##0',
} as const;


