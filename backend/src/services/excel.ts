function addHistoricalSheet(wb: ExcelJS.Workbook, fd: FinancialData): void {
  const ws = wb.addWorksheet('Historical Data');

  const years = fd.incomeStatements.map(s => String(s.year));
  setColumnWidths(ws, [32, ...years.map(() => 16)]);

  const title = ws.addRow(['HISTORICAL FINANCIALS (SEC EDGAR)', ...years]);
  styleRow(title, C.headerBg, C.headerFg, true);
  ws.addRow([]);

  modelSectionHeader(ws, 'INCOME STATEMENT', years.length);
  modelRow(ws, 'Revenue',           fd.incomeStatements.map(s => s.revenue            / 1e9), FMT.billions, false);
  modelRow(ws, 'Gross Profit',      fd.incomeStatements.map(s => s.grossProfit        / 1e9), FMT.billions, false);
  modelRow(ws, 'EBITDA',            fd.incomeStatements.map(s => s.ebitda             / 1e9), FMT.billions, false);
  modelRow(ws, 'EBIT',              fd.incomeStatements.map(s => s.ebit               / 1e9), FMT.billions, false);
  modelRow(ws, 'Net Income',        fd.incomeStatements.map(s => s.netIncome          / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'MARGINS', years.length);
  modelRow(ws, 'EBITDA Margin', fd.incomeStatements.map(s =>
    s.revenue > 0 ? s.ebitda / s.revenue : 0), FMT.pct1, false);
  modelRow(ws, 'Net Margin',    fd.incomeStatements.map(s =>
    s.revenue > 0 ? s.netIncome / s.revenue : 0), FMT.pct1, false);

  ws.addRow([]);
  modelSectionHeader(ws, 'CASH FLOW', years.length);
  modelRow(ws, 'Operating CF',       fd.cashFlows.map(c => c.operatingCashFlow   / 1e9), FMT.billions, false);
  modelRow(ws, 'CapEx',              fd.cashFlows.map(c => -c.capitalExpenditures / 1e9), FMT.billions, false);
  modelRow(ws, 'Free Cash Flow',     fd.cashFlows.map(c => c.freeCashFlow        / 1e9), FMT.billions, true);

  ws.addRow([]);
  modelSectionHeader(ws, 'BALANCE SHEET', years.length);
  modelRow(ws, 'Total Assets',    fd.balanceSheets.map(b => b.totalAssets  / 1e9), FMT.billions, false);
  modelRow(ws, 'Total Debt',      fd.balanceSheets.map(b => b.totalDebt    / 1e9), FMT.billions, false);
  modelRow(ws, 'Cash',            fd.balanceSheets.map(b => b.cash         / 1e9), FMT.billions, false);
  modelRow(ws, 'Total Equity',    fd.balanceSheets.map(b => b.totalEquity  / 1e9), FMT.billions, true);
}


// ════════════════════════════════════════════════════════════════════════════════
// Style helpers — keep all styling logic here so the sheet builders stay clean
// ════════════════════════════════════════════════════════════════════════════════

function setColumnWidths(ws: ExcelJS.Worksheet, widths: number[]): void {
  widths.forEach((w, i) => {
    ws.getColumn(i + 1).width = w;
  });
}

function styleRow(
  row: ExcelJS.Row,
  bgColor: string,
  fgColor: string,
  bold: boolean
): void {
  row.eachCell(cell => {
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + bgColor } };
    cell.font = { color: { argb: 'FF' + fgColor }, bold };
    cell.alignment = { vertical: 'middle', horizontal: 'center' };
  });
  row.height = 20;
}

function fillRow(row: ExcelJS.Row, bgColor: string, upToCol: number): void {
  for (let c = 1; c <= upToCol; c++) {
    row.getCell(c).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + bgColor } };
  }
}

function applyBorders(row: ExcelJS.Row, upToCol: number): void {
  const border: ExcelJS.Border = { style: 'thin', color: { argb: 'FF' + C.borderColor } };
  for (let c = 1; c <= upToCol; c++) {
    row.getCell(c).border = { top: border, bottom: border, left: border, right: border };
  }
}

/** Full-width section title bar */
function mergeHeader(
  ws: ExcelJS.Worksheet,
  text: string,
  startCol: number,
  endCol: number,
  rowNum: number
): void {
  const row = ws.getRow(rowNum);
  row.getCell(startCol).value = text;
  row.getCell(startCol).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + C.headerBg } };
  row.getCell(startCol).font = { color: { argb: 'FF' + C.headerFg }, bold: true, size: 13 };
  row.getCell(startCol).alignment = { vertical: 'middle', horizontal: 'left' };
  ws.mergeCells(rowNum, startCol, rowNum, endCol);
  row.height = 28;
}

/** Dark blue section header spanning the label column only */
function sectionHeader(ws: ExcelJS.Worksheet, text: string, cols: number): void {
  const row = ws.addRow([text]);
  styleRow(row, C.subheaderBg, C.subheaderFg, true);
  // Extend fill across all columns
  for (let c = 2; c <= cols; c++) {
    row.getCell(c).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + C.subheaderBg } };
  }
  row.height = 18;
}

/** Amber input cell row (2-column: label | value) */
function inputRow(ws: ExcelJS.Worksheet, label: string, value: number, fmt: string): void {
  const row = ws.addRow([label, value]);
  row.getCell(1).font = { bold: false };
  row.getCell(2).numFmt = fmt;
  row.getCell(2).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + C.inputBg } };
  row.getCell(2).alignment = { horizontal: 'right' };
  applyBorders(row, 2);
}

/** White output cell row (2-column) */
function outputRow(ws: ExcelJS.Worksheet, label: string, value: number, fmt: string): void {
  const row = ws.addRow([label, value]);
  row.getCell(2).numFmt = fmt;
  row.getCell(2).alignment = { horizontal: 'right' };
  applyBorders(row, 2);
}

/** Section header row spanning the model table width */
function modelSectionHeader(ws: ExcelJS.Worksheet, text: string, yearCount: number): void {
  const row = ws.addRow([text, ...Array(yearCount).fill(null)]);
  styleRow(row, C.subheaderBg, C.subheaderFg, true);
  row.height = 18;
}

/** A data row in the model table: label + one value per year */
function modelRow(
  ws: ExcelJS.Worksheet,
  label: string,
  values: number[],
  fmt: string,
  isTotal: boolean
): void {
  const row = ws.addRow([label, ...values]);
  row.getCell(1).font = { bold: isTotal, italic: label.startsWith('  ') };

  for (let c = 2; c <= values.length + 1; c++) {
    const cell = row.getCell(c);
    cell.numFmt = fmt;
    cell.alignment = { horizontal: 'right' };
    if (isTotal) {
      cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + C.totalBg } };
      cell.font = { bold: true };
    }
    // Red font for negative values
    if (typeof cell.value === 'number' && cell.value < 0) {
      cell.font = { ...cell.font, color: { argb: 'FF' + C.negativeFg } };
    }
    const border: ExcelJS.Border = { style: 'thin', color: { argb: 'FF' + C.borderColor } };
    cell.border = { top: border, bottom: border, left: border, right: border };
  }
  const labelBorder: ExcelJS.Border = { style: 'thin', color: { argb: 'FF' + C.borderColor } };
  row.getCell(1).border = { top: labelBorder, bottom: labelBorder, left: labelBorder, right: labelBorder };
}
